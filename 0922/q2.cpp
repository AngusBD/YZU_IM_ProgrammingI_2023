#include <iostream>

using namespace std;

int main(void) {
  int n;
  cout << "Please enter your income: ";
  cin >> n;
  cout << "The tax is ";

  if (0 <= n && n <= 8350) {
    cout << n * 0.1 << endl;
  } else if (8350 < n && n <= 33950){
    cout << 8350 * 0.1 + (n - 8350) * 0.15 << endl;
  } else if (33950 < n && n <= 82250) {
    cout << 8350 * 0.1 + (n - 8350) * 0.15 + (n - 8350 - 33950) * 0.25 << endl;
  } else if (82250 < n && n <= 171550) {
    cout << 8350 * 0.1 + (n - 8350) * 0.15 + (n - 8350 - 33950) * 0.25 +  (n - 8350 - 33950 - 82250) * 0.28 << endl;
  } else if (171550 < n && n <= 372950) {
    cout << 8350 * 0.1 + (n - 8350) * 0.15 + (n - 8350 - 33950) * 0.25 +  (n - 8350 - 33950 - 82250) * 0.28 + (n - 8350 - 33950 - 82250 - 171550) * 0.33 << endl;
  } else if (372950 < n) {
    cout << 8350 * 0.1 + (n - 8350) * 0.15 + (n - 8350 - 33950) * 0.25 +  (n - 8350 - 33950 - 82250) * 0.28 + (n - 8350 - 33950 - 82250 - 171550) * 0.33 + (n - 8350 - 33950 - 82250 - 171550 - 372950) * 0.35 << endl;
  }

  return 0;
}
